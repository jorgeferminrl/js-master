// Variables
// A variable is an information container

'use strict'

var country = "Mexico";
var continent = "America";
var antique = 2024;

// You can concatenate values using +
var countryAndContinent = country + ' ' + continent;

// You can see variable values in the console
console.log(country, continent, antique);
// You can display variable values in an alert popup
alert(countryAndContinent);

/* Hint: 
- Inspect the page during runtime, go to console and type console.log(varName) to see the value of each variable

- Inspect multiple variable values using console.log(varName1,varName2);

- You can reassign variable value in console by typing the variable name and changing its value */